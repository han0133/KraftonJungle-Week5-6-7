void alternateMergeLinkedList(LinkedList *ll1, LinkedList *ll2)
{
	if (ll1 == NULL || ll2 == NULL) // 유효성 검사 1 : 구조체가 비어있으면 세그멘테이션 폴트 발생해서 프로그램 강제 종료되는 것을 방지.
		return;

	if (ll1->head == NULL || ll2->head == NULL) // 유효성 검사 2 : 비어있는지 확인. 빈 리스트끼리 merge하는 것은 의미가 없음.
	{
		printf("ll1 or ll2 is Empty\n");
		return;
	}

	int insertPosition = 1;					 // 첫 번째로 삽입할 위치로 1을 지정
	int originalLL1Size = ll1->size; // ll1의 원본 크기를 미리 저장해둔다

	// ll2에 노드가 남아있고, 삽입할 위치가 유효한 동안 반복
	while (ll2->head != NULL && insertPosition <= originalLL1Size)
	{
		// ll2의 첫 번째 노드 값을 가져온다
		int valueToInsert = ll2->head->item;

		// printf("인덱스 %d에 값 %d 삽입\n", insertPosition, valueToInsert);

		insertNode(ll1, insertPosition, valueToInsert);
		removeNode(ll2, 0); // ll2의 첫 번째 노드 제거

		insertPosition += 2; // 다음 alternate position 업데이트 (1->3->5->7...)
		originalLL1Size++;	 // 삽입으로 인한 크기 증가 반영
	}
}

